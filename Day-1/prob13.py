def add(a,b):
    c=a+b
    return c
def mul(a,b):
    d=a-b
    return d
def div(a,b):
    e=a/b
    return e
def mod(a,b):
    f=a%b
    return f
def exp(a,b):
    g=a**b
    return g
def flo(a,b):
    h=a//b
    return h
print(add(5,3))
print(mul(5,3))
print(div(5,3))
print(mod(5,3))
print(exp(5,3))
print(flo(5,3))
